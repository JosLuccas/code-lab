<?
    # Funções Matemáticas

    # sqrt()
    # Calcula a raiz quadrada.

    echo sqrt(25); // Resultado: 5

    # pow()
    # Calcula potência

    echo pow(2, 3); // Resultado: 8 (2³)

    # exp()
    # Calcula o número de Euler (e) elevado a um valor.

    echo exp(1); // Resultado: 2.718281... (valor de e)

    # log()
    # Calcula o logaritmo natural (base e).

    echo log(10); // Resultado: ~2.30

    # log10()
    # Calcula o logaritmo na base 10.

    echo log10(100); // Resultado: 2

    # pi()
    # Retorna o valor de π (pi).

    echo pi(); // Resultado: 3.141592...

    # sin()
    # Calcula o seno de um ângulo (em radianos).

    echo sin(pi()/2); // Resultado: 1

    # cos()
    # Calcula o cosseno.

    echo cos(0); // Resultado: 1

    # tan()
    # Calcula a tangente.

    echo tan(pi()/4); // Resultado: 1

    # Funções trigonométricas (sin, cos, tan) usam radianos, não graus.
    # Se precisar converter:

    echo sin(deg2rad(90)); // Resultado: 1
?>