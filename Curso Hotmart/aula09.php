<?
    # Manipulação de Números

    # abs()
    # Retorna o valor absoluto (sem sinal).
    
    echo abs(-10); // Resultado: 10

    # round()
    # Arredonda um número.

    echo round(4.6); // Resultado: 5
    echo round(4.4); // Resultado: 4

    # ceil()
    # Arredonda sempre para cima.

    echo ceil(4.1); // Resultado: 5

    # floor()
    # Arredonda sempre para baixo.

    echo floor(4.9); // Resultado: 4

    # rand()
    # Gera um número aleatório.

    echo rand(1, 10); // Número aleatório entre 1 e 10

    # min()
    # Retorna o menor valor.

    echo min(5, 2, 8, 1); // Resultado: 1

    # max()
    # Retorna o maior valor.

    echo max(5, 2, 8, 1); // Resultado: 8

    # number_format()
    # Formata um número (ex: casas decimais).

    echo number_format(1234.5678, 2); // Resultado: 1,234.57
?>